function generateReviews() {
  const allReviews = []

  products.forEach((product, idx) => {
    let templates
    if (product.name.includes('멜라토닌')) templates = reviewTemplates.melatonin
    else if (product.name.includes('GABA')) templates = reviewTemplates.gaba
    else if (product.name.includes('테아닌')) templates = reviewTemplates.theanine
    else if (product.name.includes('발레리안')) templates = reviewTemplates.valerian
    else if (product.name.includes('마그네슘')) templates = reviewTemplates.magnesium
    else if (product.name.includes('아슈와간다')) templates = reviewTemplates.ashwagandha
    else if (product.name.includes('5-HTP')) templates = reviewTemplates.htp
    else if (product.name.includes('트립토판')) templates = reviewTemplates.tryptophan
    else if (product.name.includes('슬립 옵티마이저')) templates = reviewTemplates.sleepOptimizer
    else templates = reviewTemplates.melatonin // default

    for (let i = 0; i < 20; i++) {
      const template = templates[i % templates.length]
      const author = authors[(idx * 3 + i) % authors.length] + (i + 1)
      const rating = Math.random() > 0.2 ? 5 : 4
      const helpful = Math.floor(Math.random() * 100) + 20
      const day = String(Math.floor(Math.random() * 28) + 1).padStart(2, '0')
      const month = Math.random() > 0.5 ? '12' : '11'

      allReviews.push({
        product_id: product.id,
        source: "iHerb",
        source_review_id: `${product.code}-${String(i + 1).padStart(3, '0')}`,
        author: author,
        rating: rating,
        title: template.title,
        body: `${product.brand} ${product.name} 리뷰입니다. ${template.body}`,
        language: "KOR",
        review_date: `2025-${month}-${day}`,
        helpful_count: helpful
      })
    }
  })

  return allReviews
}

